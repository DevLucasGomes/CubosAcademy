let valor = "10";

// Soma
console.log(valor + 9);
// Subtração
console.log(valor - 9);
// Divisão
console.log(valor / 9);
// Divisão não exata
console.log(valor / 4);
// Multiplicação
console.log(valor * 9);
// Potencialização
console.log(valor ** 2);
// Restante do calculo
console.log(valor % 9);

// Podemos utilizar com texto também por exemplo

let tvalor = "Olá";
// Aqui + juntará dois textos
console.log(tvalor + "!");